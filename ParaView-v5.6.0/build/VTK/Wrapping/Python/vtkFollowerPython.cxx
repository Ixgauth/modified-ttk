static const char *PyvtkFollower_Doc =
  "vtkFollower - a subclass of actor that always faces the camera\n\n"
  "Superclass: vtkActor\n\n"
  "vtkFollower is a subclass of vtkActor that always follows its\n"
  "specified camera. More specifically it will not change its position\n"
  "or scale, but it will continually update its orientation so that it\n"
  "is right side up and facing the camera. This is typically used for\n"
  "text labels in a scene. All of the adjustments that can be made to an\n"
  "actor also will take effect with a follower.  So, if you change the\n"
  "orientation of the follower by 90 degrees, then it will follow the\n"
  "camera, but be off by 90 degrees.\n\n"
  "@sa\n"
  "vtkActor vtkCamera vtkAxisFollower vtkProp3DFollower\n\n";


