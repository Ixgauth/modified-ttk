static const char *PyvtkImplicitHalo_Doc =
  "vtkImplicitHalo - implicit function for an halo\n\n"
  "Superclass: vtkImplicitFunction\n\n"
  "vtkImplicitHalo evaluates to 1.0 for each position in the sphere of a\n"
  "given center and radius Radius*(1-FadeOut). It evaluates to 0.0 for\n"
  "each position out the sphere of a given Center and radius Radius. It\n"
  "fades out linearly from 1.0 to 0.0 for points in a radius from\n"
  "Radius*(1-FadeOut) to Radius. vtkImplicitHalo is a concrete\n"
  "implementation of vtkImplicitFunction. It is useful as an input to\n"
  "vtkSampleFunction to generate an 2D image of an halo. It is used this\n"
  "way by vtkShadowMapPass.\n"
  "@warning\n"
  "It does not implement the gradient.\n\n";


